//# .insertAdjacentText() (Добавляет текст в html)
// Добавляет текст в документ и не затрагивает существующие элементы.

//# значения
{/* beforebegin */}
<div>
  {/* afterbegin */}
  текущая разметка
  {/* beforeend */}
</div>;
{/* afterend */}

//# пример
zoo.insertAdjacentText('afterbegin', '12345');
