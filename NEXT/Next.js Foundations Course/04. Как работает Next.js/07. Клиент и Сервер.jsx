//# Клиент и Сервер
// В контексте веб-приложений клиентом называется браузер на устройстве пользователя, который отправляет на сервер запрос кода вашего приложения. Затем он превращает ответ, полученный от сервера, в интерфейс, с которым может взаимодействовать пользователь.

// Сервер — это компьютер в центре обработки данных, который хранит код вашего приложения, получает запросы от клиента, выполняет некоторые вычисления и отправляет обратно соответствующий ответ.
