//# Атрибут src (URL АДРЕС)
// Указывает путь к файлу со скриптами. Это может быть полный URL-адрес файла или относительный адрес, например, src="/scripts/browser.js". Файл должен иметь расширение .js. Когда указываешь src, не пиши ничего внутри <script>...</script>.

//# Пример
<script src="script.js"></script>;
