// Сцепление символов строки и той сущности, с которой строка соединяется.

//# пример
//* сложение строки со строкой
console.log('вино' + 'град'); // string "виноград"

//* сложение строки с числом
console.log(100 + '500'); // string "100500"

//* сложение числа с числом
console.log(100 + 500); // number 600
