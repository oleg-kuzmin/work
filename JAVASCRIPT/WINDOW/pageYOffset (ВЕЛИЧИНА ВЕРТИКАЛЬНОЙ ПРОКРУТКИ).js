//# window.pageYOffset (ВЕЛИЧИНА ВЕРТИКАЛЬНОЙ ПРОКРУТКИ)
// Содержит число пикселей, на которое пользователь прокрутил страницу по вертикали.

//# Возвращает
//* Число с значением вертикальной прокрутки.

//# Пример
// Если мы на самом верху страницы
console.log(window.pageYOffset); // Выведет: 0

// Прокрутим страницу на 200px
console.log(window.pageYOffset); // Выведет: 200
