//# charAt() (ВОЗВРАЩАЕТ СИМВОЛ ПО ИНДЕКСУ)
// Метод charAt() возвращает указанный символ из строки.

//# Синтаксис
string.charAt(index);
//* Число: index - целое число от 0 до (string.length - 1).

//# Возвращает
//* Строка с символом || Пустая строка

//# Пример
const anyString = 'Дивный новый мир';
anyString.charAt(0); // Д
anyString.charAt(1); // и
anyString.charAt(2); // в
anyString.charAt(3); // н
anyString.charAt(4); // ы
anyString.charAt(5); // й
