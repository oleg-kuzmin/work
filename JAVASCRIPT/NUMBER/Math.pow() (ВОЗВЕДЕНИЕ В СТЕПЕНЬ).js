//# Math.pow() (ВОЗВЕДЕНИЕ В СТЕПЕНЬ)
// Метод возвращает число, возведённое в степень.

//# Синтаксис
//* 1. Число: base - основание
//* 2. Число: exponent - степень, в которую возводится основание
Math.pow(base, exponent);

//# Возвращает
//* Число - возведённое в степень.

//# Пример
Math.pow(10, 3); // 1000
