//# confirm (Модальное окно с подтверждением)
// Отображает модальное окно с текстом вопроса question и двумя кнопками: OK и Отмена.

//# пример
// возвращает true, если нажата кнопка OK. В других случаях – false.
let isBoss = confirm('Ты здесь главный?');
console.log(isBoss); // true, если нажата OK
